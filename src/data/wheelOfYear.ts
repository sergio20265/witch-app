/** День года (0–365) для сравнения дат внутри одного цикла. */
function dayOfYear(month: number, day: number): number {
  const days = [0, 31, 59, 90, 120, 151, 181, 212, 243, 273, 304, 334];
  return days[month - 1] + day;
}

/** Ближайший предстоящий праздник от заданной даты. */
export function nextSabbat(from = new Date()): { sabbat: Sabbat; daysUntil: number } {
  const today = dayOfYear(from.getMonth() + 1, from.getDate());
  let best: Sabbat | null = null;
  let bestDelta = Infinity;
  for (const s of wheelOfYear) {
    let delta = dayOfYear(s.date.month, s.date.day) - today;
    if (delta < 0) delta += 365;
    if (delta < bestDelta) {
      bestDelta = delta;
      best = s;
    }
  }
  return { sabbat: best!, daysUntil: bestDelta };
}

