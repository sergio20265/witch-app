// Странники тропы — «человеческий» слой встреч.
//
// В отличие от драконов и Хранителя, странники — люди (или почти люди): бродячие
// мастера, торговки, чужие ведьмы. Встреча повторяется (с кулдауном), не привязана
// к типажу и даёт бартер находок, обучение чужому ремеслу или тихую подсказку.
//
// Модель проще события: у странника есть набор сценок, у сценки — 2-3 выбора.
// Выбор двигает склонность, внимание пути, изредка дарит находку или бонус-шаг.
// Бартерные выборы с `requiresTrinket` показываются, только если вещь есть в котомке.

export interface WandererChoice {
  text: string;
  outcome: string;
  affinity?: Record<string, number>;
  attention?: number;             // сдвиг внимания пути
  bonusSteps?: number;            // доп. шаг сверх темпа
  grantTrinket?: string;          // находка в котомку (если ещё нет)
  requiresTrinket?: string;       // выбор доступен, только если эта вещь уже в котомке (бартер)
}

export interface WandererScene {
  title: string;
  text: string;
  choices: WandererChoice[];
}

export interface Wanderer {
  id: string;
  name: string;
  glyph: string;
  blurb: string;
  art: string;
  scenes: WandererScene[];
}

