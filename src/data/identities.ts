/* ====================================================================
   Пути гримуара — что меняется в зависимости от выбранного типажа ведьмы.

   Типаж (`userIdentity`) выбирается в онбординге и в Settings. Здесь собран
   весь контент, который он окрашивает: кредо и шёпот пути на главной,
   описание в настройках/онбординге, избранные тропинки, мягкий цветовой
   акцент и то, как путь «прочитывает» карту дня.

   Тон — образный и конкретный, без банальностей. Добавляя типаж, просто
   дополни массив IDENTITIES — Home, онбординг и настройки подхватят.
   ==================================================================== */

export interface Identity {
  id: string;
  label: string;
  glyph: string;
  /** Стихия / покровитель пути — короткая подпись. */
  element: string;
  /** Мягкий цветовой акцент пути (--path-accent). */
  accent: string;
  /** Кредо — рукописная строка-эпиграф на главной. */
  credo: string;
  /** Описание типажа — показывается в онбординге и в настройках. */
  description: string;
  /** Тропинки (`to`), которые этому пути ближе — поднимаются вперёд. */
  favored: string[];
  /** «Шёпот пути» — тихие строки, меняются каждый день по кругу. */
  whispers: string[];
  /** Как этот путь прочитывает карту дня (тоже по кругу). */
  cardLens: string[];
}

