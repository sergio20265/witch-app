// Старые id, переехавшие в новые типажи — чтобы не потерять прежний выбор.
const ALIASES: Record<string, string> = {
  hermit: 'hedge',
  guardian: 'hearth',
  naturalist: 'storm',
  'forest-witch': 'green',
  herbalist: 'green',
};

const BY_ID = new Map(IDENTITIES.map((i) => [i.id, i]));
const FALLBACK: Identity = BY_ID.get('green')!; // дух приложения

/** Путь по id; учитывает миграцию старых id и безопасный фолбэк. */
export function identityFor(id: string | undefined | null): Identity {
  if (!id) return FALLBACK;
  return BY_ID.get(ALIASES[id] ?? id) ?? FALLBACK;
}

/** Детерминированный выбор по дню — одна строка на сутки. */
function pickByDay(list: string[], salt = 0): string {
  if (list.length === 0) return '';
  const d = new Date();
  const dayNum = Math.floor(Date.UTC(d.getFullYear(), d.getMonth(), d.getDate()) / 86400000);
  return list[(dayNum + salt) % list.length];
}

/** Сегодняшний «шёпот пути». */
export function pathWhisper(id: string | undefined | null): string {
  return pickByDay(identityFor(id).whispers);
}

/** Шёпот с учётом перенятых ремёсел (навыков): подмешивает их пулы. */
export function blendedPathWhisper(ids: string[]): string {
  const seen = new Set<string>();
  const pool: string[] = [];
  for (const id of ids) {
    const key = ALIASES[id] ?? id;
    if (seen.has(key)) continue;
    seen.add(key);
    pool.push(...identityFor(id).whispers);
  }
  return pickByDay(pool);
}

/** Как путь прочитывает карту дня сегодня. */
export function cardLensFor(id: string | undefined | null): string {
  return pickByDay(identityFor(id).cardLens, 3);
}

