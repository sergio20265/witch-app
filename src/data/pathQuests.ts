// Многошаговые мини-квесты тропы.
//
// В отличие от события (одно дерево за один шаг), квест — цепочка из 2-4 стадий,
// растянутая во времени: каждая стадия выходит навстречу отдельным шагом, через
// несколько обычных шагов после предыдущей (delaySteps). Между стадиями есть
// память — поздняя стадия может требовать находку, полученную в ранней
// (requiresTrinket), — так получается «найти → отнести → получить».
//
// Стадия 0 — завязка (hook): среди её выборов есть согласие (двигает квест) и
// отказ (advance: false — квест не начинается, ставится короткий кулдаун).
// Последняя стадия — финал: её награда крупнее (оберег + бонус-шаги).

export interface QuestChoice {
  text: string;
  outcome: string;
  affinity?: Record<string, number>;
  attention?: number;             // сдвиг внимания пути
  bonusSteps?: number;            // доп. шаги сверх темпа
  grantTrinket?: string;          // находка в котомку (если ещё нет)
  requiresTrinket?: string;       // выбор доступен, только если вещь уже в котомке (память квеста)
  requiresSkill?: string | string[]; // выбор доступен, только если это ремесло — своё или перенято (крафт-концовки)
  advance?: boolean;              // false — не двигать/не начинать квест (отказ). По умолчанию true.
}

export interface QuestStage {
  id: string;
  title: string;
  text: string;
  art?: string;
  choices: QuestChoice[];
  delaySteps?: number;            // через сколько обычных шагов эта стадия выйдет навстречу (по умолчанию 2)
}

export interface PathQuest {
  id: string;
  name: string;
  glyph: string;
  hint: string;                   // краткое описание для журнала странствия
  tracks?: string[];              // каким типажам доступна завязка ('*' или пусто — всем)
  requireIdentity?: string[];
  minStep?: number;               // не раньше N-го пройденного шага
  minAffinity?: { id: string; value: number };
  stages: QuestStage[];           // по порядку; последняя — финал
  art?: string;
}

